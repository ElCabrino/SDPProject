package ch.epfl.sweng.vanjel;

public class DoctorCalendarItem {

    private String head;
    private String desc;

    public DoctorCalendarItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }


    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
